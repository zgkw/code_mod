
//-----  快速幂 逆元  ---------------------------------------------------------------------------------------------------------
	//
ll Q_pow ( ll a , ll b = mod , ll p = mod ) // a 对 b (一个素数） 的 逆元（p为答案限制的mod）
{
	ll ans = 1 ;

	b = b - 2 ;

	a = ( a % p + p ) % p ;

	for ( ; b ; b >>= 1 )
	{
		if ( b & 1 ) ans = ( a * ans ) % p ; 	// a * ans 可能会爆

		a = ( a * a ) % p ; 					// 同理
	}
	return ans ;
}

